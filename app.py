"""
Module contenant une fonction de multiplication simple.
"""

def multiplication(valeur, multiplicateur):
    """
    Multiplie deux nombres et retourne le résultat.

    Args:
        valeur (int | float): La valeur à multiplier.
        multiplicateur (int | float): Le facteur multiplicateur.

    Returns:
        int | float: Le produit des deux nombres.
    """
    return valeur * multiplicateur

print(multiplication(2, 5))

